
BEGIN EXECUTE IMMEDIATE 'DROP TABLE LOG_AUDITORIA CASCADE CONSTRAINTS'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP TABLE INSCRICOES CASCADE CONSTRAINTS';    EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP TABLE USUARIOS CASCADE CONSTRAINTS';      EXCEPTION WHEN OTHERS THEN NULL; END;
/


CREATE TABLE USUARIOS (
    ID         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOME       VARCHAR2(100)  NOT NULL,
    EMAIL      VARCHAR2(150)  NOT NULL UNIQUE,
    PRIORIDADE NUMBER(1)      NOT NULL
                              CHECK (PRIORIDADE IN (1, 2, 3)),
    SALDO      NUMBER(10, 2)  DEFAULT 0 NOT NULL
);


CREATE TABLE INSCRICOES (
    ID          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USUARIO_ID  NUMBER        NOT NULL
                              REFERENCES USUARIOS(ID),
    STATUS      VARCHAR2(20)  DEFAULT 'PENDENTE'
                              CHECK (STATUS IN ('PRESENT','ATIVO','CANCELADO','PENDENTE')),
    VALOR_PAGO  NUMBER(10, 2) NOT NULL
                              CHECK (VALOR_PAGO >= 0),
    TIPO        VARCHAR2(50)  DEFAULT 'PADRAO'
);

CREATE TABLE LOG_AUDITORIA (
    ID           NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    INSCRICAO_ID NUMBER        NOT NULL
                               REFERENCES INSCRICOES(ID),
    MOTIVO       VARCHAR2(400) NOT NULL,
    DATA         DATE          DEFAULT SYSDATE NOT NULL
);

COMMIT;
